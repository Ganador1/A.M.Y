class PublicationPackager:
    """Publication packaging and artifact management"""
    
    def __init__(self, base_path: str = "./publications"):
        self.base_path = Path(base_path)
        self.base_path.mkdir(parents=True, exist_ok=True)
    
    def create_package_structure(self, pub_id: str) -> Path:
        """Create directory structure for publication package"""
        package_path = self.base_path / f"publication_{pub_id}"
        package_path.mkdir(parents=True, exist_ok=True)
        
        # Create subdirectories
        (package_path / "figures").mkdir(exist_ok=True)
        (package_path / "data").mkdir(exist_ok=True)
        (package_path / "models").mkdir(exist_ok=True)
        
        return package_path
    
    def calculate_package_hash(self, package_path: Path) -> str:
        """Calculate BLAKE3 hash of entire package"""
        import hashlib
        
        hasher = hashlib.blake2b()
        
        # Hash all files in package in sorted order
        for file_path in sorted(package_path.rglob("*")):
            if file_path.is_file():
                hasher.update(str(file_path.relative_to(package_path)).encode())
                hasher.update(file_path.read_bytes())
        
        return hasher.hexdigest()
    
    def create_manifest(self, package: PublicationPackage) -> CreateManifestResult:
        """Create publication manifest"""
        return {
            "schema_version": "publication.manifest.v1",
            "pub_id": package.metadata.pub_id,
            "doi": package.metadata.doi,
            "title": package.metadata.title,
            "authors": package.metadata.authors,
            "domains": package.metadata.domains,
            "created_at": package.metadata.created_at.isoformat(),
            "package_hash": package.package_hash,
            "integrity_proof": package.integrity_proof,
            "artifacts": package.artifacts,
            "hypothesis_id": package.metadata.hypothesis_id,
            "research_cycle_id": package.metadata.research_cycle_id,
            "consensus_score": package.metadata.consensus_score,
            "blockchain_proof": package.metadata.blockchain_proof
        }


class PublicationGeneratorService(BaseService):
    """Main service for automated scientific publication generation"""
    
    def __init__(self):
        super().__init__("PublicationGeneratorService")
        self.template_engine = IMRaDTemplateEngine()
        self.packager = PublicationPackager()
        self.blockchain_service = BlockchainValidationService()
        self.figure_generator = ScientificFigureGenerator()
        self.journal_formatter = JournalFormatterService()
        self.supplementary_generator = SupplementaryMaterialsGenerator()
        self.persistence = None
        
        # Try to initialize hypothesis persistence
        try:
            self.persistence = HypothesisPersistenceService()
        except BiologyError:
            logger.warning("⚠️ Hypothesis persistence not available")
        
        logger.info("✅ PublicationGeneratorService initialized with figure generation, journal formatting, and supplementary materials")
    
    async def process_request(self, request_data: ProcessRequestResult) -> ProcessRequestResult:
        """Process publication generation requests"""
        try:
            action = request_data.get("action", "")
            
            if action == "generate_publication":
                return await self.generate_publication(request_data)
            elif action == "get_publication":
                return await self.get_publication(request_data)
            elif action == "list_publications":
                return await self.list_publications(request_data)
            elif action == "validate_publication":
                return await self.validate_publication(request_data)
            elif action == "generate_journal_formatted_publication":
                return await self.generate_journal_formatted_publication(request_data)
            elif action == "generate_publication_with_supplementary":
                return await self.generate_publication_with_supplementary(request_data)
            else:
                return {
                    "success": False,
                    "error": f"Unknown action: {action}",
                    "available_actions": [
                        "generate_publication", "get_publication", 
                        "list_publications", "validate_publication",
                        "generate_journal_formatted_publication",
                        "generate_publication_with_supplementary"
                    ]
                }
                
        except BiologyError as e:
            return self.handle_error(e, "process_request")
    
    async def generate_publication(self, request_data: GeneratePublicationResult) -> GeneratePublicationResult:
        """Generate complete publication package from hypothesis or research cycle"""
        try:
            hypothesis_id = request_data.get("hypothesis_id")
            research_cycle_id = request_data.get("research_cycle_id")
            title = request_data.get("title")
            custom_content = request_data.get("custom_content", {})
            
            if not hypothesis_id and not title:
                return {
                    "success": False,
                    "error": "Either hypothesis_id or title must be provided"
                }
            
            # Generate publication ID
            pub_id = str(uuid.uuid4())[:8]
            
            # Gather content from various sources
            content_data = await self._gather_publication_content(
                hypothesis_id, research_cycle_id, custom_content
            )
            
            # Create metadata
            metadata = PublicationMetadata(
                pub_id=pub_id,
                title=title or content_data.get("title", f"Research Publication {pub_id}"),
                abstract=content_data.get("abstract", ""),
                domains=content_data.get("domains", []),
                keywords=content_data.get("keywords", []),
                hypothesis_id=hypothesis_id,
                research_cycle_id=research_cycle_id,
                consensus_score=content_data.get("consensus_score", 0.0)
            )
            
            # Generate content using templates
            content = await self._generate_imrad_content(metadata, content_data)
            
            # Create package structure
            package_path = self.packager.create_package_structure(pub_id)
            
            # Generate figures automatically
            figures = await self._generate_publication_figures(content_data, package_path, metadata)
            content.figures = figures
            
            # Write content files
            await self._write_content_files(package_path, content, metadata)
            
            # Calculate package hash
            package_hash = self.packager.calculate_package_hash(package_path)
            
            # Generate DOI
            doi = DOIGenerator.generate_doi(package_hash)
            metadata.doi = doi
            
            # Blockchain validation
            integrity_proof = await self._create_integrity_proof(package_path, package_hash)
            metadata.blockchain_proof = integrity_proof.get("blockchain_proof")
            
            # Create final package
            package = PublicationPackage(
                metadata=metadata,
                content=content,
                package_path=package_path,
                package_hash=package_hash,
                integrity_proof=integrity_proof
            )
            
            # Write manifest and final files
            await self._finalize_package(package)
            
            logger.info(f"✅ Publication generated: {pub_id} - DOI: {doi}")
            
            return {
                "success": True,
                "pub_id": pub_id,
                "doi": doi,
                "package_path": str(package_path),
                "package_hash": package_hash,
                "title": metadata.title,
                "created_at": metadata.created_at.isoformat(),
                "integrity_proof": integrity_proof
            }
            
        except BiologyError as e:
            return self.handle_error(e, "generate_publication")
    
    async def _gather_publication_content(self, hypothesis_id: Optional[str], 
                                        research_cycle_id: Optional[str],
                                        custom_content: Dict[str, Any]) -> Dict[str, Any]:
        """Gather content from various sources for publication"""
        content = {
            "title": custom_content.get("title", "Autonomous Scientific Research"),
            "abstract": custom_content.get("abstract", "Generated by AXIOM autonomous research."),
            "domains": custom_content.get("domains", ["computational_science"]),
            "keywords": custom_content.get("keywords", ["autonomous_research", "ai_discovery"]),
            "consensus_score": 0.0,
            "validation_results": {},
            "experimental_data": {},
            "hypothesis_validation": {}
        }
        
        # Gather hypothesis data if available
        if hypothesis_id and self.persistence:
            try:
                hypothesis_data = await self._get_hypothesis_data(hypothesis_id)
                if hypothesis_data:
                    content.update({
                        "title": hypothesis_data.get("title", content["title"]),
                        "abstract": hypothesis_data.get("description", content["abstract"]),
                        "domains": hypothesis_data.get("domain", [content["domains"][0]]),
                        "hypothesis_validation": {
                            "status": hypothesis_data.get("status", "unknown"),
                            "confidence": hypothesis_data.get("confidence_score", 0.0),
                            "evidence_strength": hypothesis_data.get("evidence_strength", 0.0)
                        }
                    })
            except BiologyError as e:
                logger.warning(f"⚠️ Could not fetch hypothesis data: {e}")
        
        # Gather cross-validation results
        try:
            validation_services = ["ScientificHypothesisAgent", "ResearchCycleManager", "LiteratureSearchService"]
            validation_run = await operational_matrix.validate_cross_compatibility(validation_services)
            content["validation_results"] = {
                "aggregate_score": validation_run.aggregate_score,
                "confidence": validation_run.uncertainty_metrics.get("confidence_mean", 0.0),
                "uncertainty": validation_run.uncertainty_metrics.get("uncertainty_mean", 0.0),
                "individual_scores": [
                    {
                        "domain": score.domain.value if hasattr(score.domain, 'value') else str(score.domain),
                        "score": float(score.score) if score.score is not None else 0.0,
                        "confidence": float(score.confidence) if score.confidence is not None else 0.0,
                        "uncertainty": float(score.uncertainty) if score.uncertainty is not None else 0.0
                    } for score in validation_run.individual_scores[:5]  # Limit to 5 for readability
                ]
            }
            content["consensus_score"] = validation_run.aggregate_score
        except BiologyError as e:
            logger.warning(f"⚠️ Could not gather validation results: {e}")
        
        return content
    
