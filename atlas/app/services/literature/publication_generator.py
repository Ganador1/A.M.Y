    async def validate_publication(self, request_data: ValidatePublicationResult) -> ValidatePublicationResult:
        """Validate publication integrity"""
        try:
            pub_id = request_data.get("pub_id")
            if not pub_id:
                return {"success": False, "error": "pub_id is required"}
            
            package_path = self.packager.base_path / f"publication_{pub_id}"
            if not package_path.exists():
                return {"success": False, "error": f"Publication {pub_id} not found"}
            
            # Calculate current hash
            current_hash = self.packager.calculate_package_hash(package_path)
            
            # Read stored hash
            hash_file = package_path / "package_hash.txt"
            if hash_file.exists():
                stored_hash = hash_file.read_text().strip()
                hash_valid = current_hash == stored_hash
            else:
                hash_valid = False
                stored_hash = "missing"
            
            # Check integrity proof
            integrity_file = package_path / "integrity_proof.json"
            if integrity_file.exists():
                integrity_proof = json.loads(integrity_file.read_text())
                blockchain_valid = integrity_proof.get("blockchain_validation", False)
            else:
                integrity_proof = {}
                blockchain_valid = False
            
            return {
                "success": True,
                "pub_id": pub_id,
                "validation": {
                    "hash_valid": hash_valid,
                    "blockchain_valid": blockchain_valid,
                    "current_hash": current_hash,
                    "stored_hash": stored_hash,
                    "integrity_proof": integrity_proof
                }
            }
            
        except BiologyError as e:
            return self.handle_error(e, "validate_publication")
    
