class DOIGenerator:
    """Internal DOI generation system"""
    
    @staticmethod
    def generate_doi(content_hash: str, year: Optional[int] = None) -> str:
        """Generate internal DOI: axiom:YYYY:hash_prefix"""
        if year is None:
            year = datetime.now().year
        
        # Use first 12 characters of hash for DOI
        hash_prefix = content_hash[:12]
        return f"axiom:{year}:{hash_prefix}"
    
    @staticmethod
    def validate_doi(doi: str) -> bool:
        """Validate DOI format"""
        parts = doi.split(":")
        return len(parts) == 3 and parts[0] == "axiom" and len(parts[2]) == 12


