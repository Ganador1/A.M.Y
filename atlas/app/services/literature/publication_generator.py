    async def _finalize_package(self, package: PublicationPackage):
        """Finalize publication package with manifest and hash"""
        
        # Update package hash in metadata files
        package_hash_updated = self.packager.calculate_package_hash(package.package_path)
        package.package_hash = package_hash_updated
        
        # Write final manifest
        manifest = self.packager.create_manifest(package)
        (package.package_path / "manifest.json").write_text(json.dumps(manifest, indent=2))
        
        # Write package hash file
        (package.package_path / "package_hash.txt").write_text(package_hash_updated)
        
        logger.info(f"📦 Package finalized: {package.package_path}")
    
